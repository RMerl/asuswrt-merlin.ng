static uint16
phy_ac_read_phyreg(phy_info_t *pi, phy_type_info_t *ti, uint addr)
{
	/*
	 * The TableDataWide register is only valid to read once the TableID and
	 * TableOffset registers are set.
	 */
	if (addr != ACPHY_TableDataWide(pi->pubpi.phy_rev)) {
		return phy_utils_read_phyreg(pi, (uint16)addr);
	}

	return 0;
}

static int
phy_ac_dump_phyregs(phy_info_t *pi, phy_type_info_t *ti, struct bcmstrbuf *b)
{
	phy_regs_t *rl = NULL;

	wlc_phy_stay_in_carriersearch_acphy(pi, TRUE);

	if (ACMAJORREV_32(pi->pubpi.phy_rev) || ACMAJORREV_33(pi->pubpi.phy_rev)) {
		rl = acphy32_regs;
	} else if (ACMAJORREV_5(pi->pubpi.phy_rev)) {
		rl = acphy9_regs;
	} else if (ACMAJORREV_4(pi->pubpi.phy_rev)) {
		uint16 phymode = phy_get_phymode(pi);
		if (phymode == PHYMODE_RSDB)
			rl = acphy12_regs_rsdb;
		else if (phymode == PHYMODE_MIMO)
			rl = acphy12_regs_mimo;
		else if (phymode == PHYMODE_80P80)
			rl = acphy12_regs_80p80;
		else
			ASSERT(0);
	} else if (ACMAJORREV_3(pi->pubpi.phy_rev)) {
		rl = acphy13_regs;
	} else if (ACMAJORREV_2(pi->pubpi.phy_rev)) {
		rl = acphy3_regs;
	} else if (ACMAJORREV_1(pi->pubpi.phy_rev)) {
		rl = acphy2_regs;
	} else if (ACMAJORREV_0(pi->pubpi.phy_rev)) {
		rl = acphy0_regs;
	} else {
		ASSERT(0);
	}
	phy_dump_phyregs(pi, "acphy", rl, 0, b);

	wlc_phy_stay_in_carriersearch_acphy(pi, FALSE);

	return BCME_OK;
}
