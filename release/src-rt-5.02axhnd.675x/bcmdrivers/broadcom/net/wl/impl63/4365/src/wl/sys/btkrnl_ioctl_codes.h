// if succeeds.  May be called at high execution priority levels.
#define BTKRNL_IOCTL_DEVICE_DATA         CTL_CODE(DD_TYPE_BTKRNL_BTW, IOCTL_BTKRNL_BASE+0x31, METHOD_BUFFERED, FILE_ANY_ACCESS)

