#define BTKRNL_IOCTL_DEVICE_STOP         CTL_CODE(DD_TYPE_BTKRNL_BTW, IOCTL_BTKRNL_BASE+0x34, METHOD_BUFFERED, FILE_ANY_ACCESS)

