static void ikos_net_dump_packet(unsigned char *data_ptr, unsigned short size)
{
	int i;

	for (i = 0; i < size; i++) {
		if ((i & 0xf) == 0)
			printf("0x%x:", i);
		if ((i & 0x3) == 0)
			printf(" ");
		printf("%02x ", data_ptr[i]);
		if ((i & 0xf) == 0xf)
			printf("\n");
	}
	if ((i & 0xf) != 0)
		printf("\n");
}

static void send_recieve_iter_pckts( int  port_id, unsigned int  packet_size, int iteration)
{
	int i;
    int rc = 0;
    unsigned char *rx_data_buf;
    unsigned short length = 0;
    unsigned char rx_port;

    for (i = 0; i < iteration; i++)
    {

        rc = bcmbca_xrdp_send(ikos_tx_data, packet_size, QEMU_ACTIVE_PORT);
        printf("==>sent pckt[%d] to port %d\n", i, QEMU_ACTIVE_PORT);
        if (rc)
        {
            printf("===>Error:sent pckt[%d]\n", i);  
            goto out;      
        }
	
		rx_data_buf = net_rx_packets[0];
        if(bcmbca_xrdp_recv(&rx_data_buf, &length, &rx_port))
		{
			printf("===>Error 1: packet[%d] length[%d]\n", i, length);
			goto out;
		}
        if(length != packet_size)
        {
            printf("===>Error 2: packet[%d] length[%d]\n", i, length);
            goto out;
        } 

        if (memcmp(ikos_tx_data, rx_data_buf, packet_size) != 0)
	    {
	        printf("===>Error: packet[%d] with length[%d] not equal\n", i,packet_size);
            ikos_net_dump_packet(rx_data_buf, packet_size);
            goto out;
        }
	
	    printf("===>Recieved packet[%d] length[%d] from port[%d] is OK and Equal!!!!\n", i, length,QEMU_ACTIVE_PORT);
out:
        length = 0; 
		     
    }

    return;
}

void run_ikos_net_test(int port_id, unsigned int  packet_size, int iteration)
{
	send_recieve_iter_pckts(port_id, packet_size, iteration);

    printf("EXIT:%s()\n",__FUNCTION__);
}

