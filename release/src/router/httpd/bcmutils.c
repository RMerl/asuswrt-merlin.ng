#define __ismask(x) (_ctype[(int)(unsigned char)(x)])

#define isalnum(c)	((__ismask(c)&(_U|_L|_D)) != 0)
#define isalpha(c)	((__ismask(c)&(_U|_L)) != 0)
#define iscntrl(c)	((__ismask(c)&(_C)) != 0)
#define isdigit(c)	((__ismask(c)&(_D)) != 0)
#define isgraph(c)	((__ismask(c)&(_P|_U|_L|_D)) != 0)
#define islower(c)	((__ismask(c)&(_L)) != 0)
#define isprint(c)	((__ismask(c)&(_P|_U|_L|_D|_SP)) != 0)
#define ispunct(c)	((__ismask(c)&(_P)) != 0)
#define isspace(c)	((__ismask(c)&(_S)) != 0)
#define isupper(c)	((__ismask(c)&(_U)) != 0)
#define isxdigit(c)	((__ismask(c)&(_D|_X)) != 0)

static uchar
bcm_toupper(uchar c)
{
	if (islower(c))
		c -= 'a'-'A';
	return (c);
}

ulong
bcm_strtoul(char *cp, char **endp, uint base)
{
	ulong result, value;

	/* The real strtoul supports a base of 0, but we don't */
	ASSERT(base);

	result = 0;

	while (isxdigit(*cp) &&
	       (value = isdigit(*cp) ? *cp-'0' : bcm_toupper(*cp)-'A'+10) < base) {
		result = result*base + value;
		cp++;
	}
	if (endp)
		*endp = (char *)cp;

	return (result);
}

uint
bcm_atoi(char *s)
{
	uint n;

	n = 0;

	while (isdigit(*s))
		n = (n * 10) + *s++ - '0';
	return (n);
}

void
deadbeef(char *p, uint len)
{
	static uchar meat[] = { 0xde, 0xad, 0xbe, 0xef };

	while (len-- > 0) {
		*p = meat[((uint)p) & 3];
		p++;
	}
}

uchar*
bcm_ether_ntoa(char *ea, char *buf, const size_t buf_sz)
{
	snprintf(buf, buf_sz, "%x:%x:%x:%x:%x:%x",
		ea[0]&0xff, ea[1]&0xff, ea[2]&0xff, ea[3]&0xff, ea[4]&0xff, ea[5]&0xff);
	return (buf);
}

/* parse a xx:xx:xx:xx:xx:xx format ethernet address */
void
bcm_ether_atoe(char *p, char *ea)
{
	int i;

	for (i = 0; i < 6; i++) {
		ea[i] = (char) bcm_strtoul(p, &p, 16);
		if (*p == '\0')	/* just bail on error */
			break;
		p++;
	}
}

int
bcm_isprint(uchar c)
{
	return (isprint(c));
}

/* 
 * Traverse a string of 1-byte tag/1-byte length/variable-length value 
 * triples, returning a pointer to the substring whose first element 
 * matches tag
 */
uint8 *
bcm_parse_tlvs(uint8 *buf, int buflen, uint key)
{
	uint8 *cp;
	int totlen;

	cp = buf;
	totlen = buflen;

	/* find tagged parameter */
	while (totlen > 0) {
		uint tag, len;

		tag = *cp;

		if (tag == key)
			return (cp);

		len = *(cp + 1);
		cp += (len + 2);
		totlen -= (len + 2);
	}
	
	return NULL;
}

#define	pktq_next(i)	((i+1) & (MAXQLEN-1))
#define	pktq_prev(i)	((i-1) & (MAXQLEN-1))

void
pktqinit(struct pktq *q)
{
	q->head = q->tail = 0;
	bzero((char*)q->p, sizeof (q->p));
}

void
pktenq(struct pktq *q, void *p)
{
	ASSERT(p);
	ASSERT(!pktq_full(q));

	ASSERT(q->p[q->tail] == NULL);
	q->p[q->tail] = p;
	q->tail = pktq_next(q->tail);
}

void*
pktdeq(struct pktq *q)
{
	void *p;

	if (q->head == q->tail)
		return (NULL);

	p = q->p[q->head];
	ASSERT(p);

	q->p[q->head] = NULL;
	q->head = pktq_next(q->head);

	return (p);
}

